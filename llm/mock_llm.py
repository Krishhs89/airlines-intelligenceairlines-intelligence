    revenue_idx = v.get("revenue_index", 1.0)
    competition = v.get("competition_level", "Medium")
    actual_load = v.get("actual_load", 75)
    actual_otp = v.get("actual_otp", 80)
    
    parts = []
    
    # Demand insights
    if demand >= 0.80:
        parts.append(f"This is one of our strongest demand corridors with a demand score of {demand:.2f}.")
        parts.append("Market research indicates consistent business and leisure travel throughout the year.")
    elif demand >= 0.50:
        parts.append(f"Moderate demand profile (score: {demand:.2f}) with seasonal peaks during summer and holidays.")
        parts.append("Revenue opportunity exists through tactical capacity and frequency optimization.")
    else:
        parts.append(f"Lower demand environment (score: {demand:.2f}) requires strategic review.")
        parts.append("Consider seasonal scheduling or partnership opportunities to maintain market presence.")
    
    # Revenue analysis
    if revenue_idx >= 1.3:
        parts.append(f"Premium yield market with revenue index {revenue_idx:.2f}x system average. Business travel dominates.")
    elif revenue_idx >= 0.9:
        parts.append(f"Standard revenue dynamics (index: {revenue_idx:.2f}x) with balanced leisure/business mix.")
    else:
        parts.append(f"Below-average revenue realization (index: {revenue_idx:.2f}x) suggests price sensitivity.")
    
    # Competitive assessment
    if competition == "High":
        parts.append(f"High competitive intensity with 3+ major carriers. Differentiation through schedule or service is critical.")
    elif competition == "Medium":
        parts.append(f"Moderate competition allows for selective capacity growth if demand supports.")
    else:
        parts.append(f"Limited direct competition presents opportunity for market expansion.")
    
    # Performance metrics
    if actual_load >= 85:
        parts.append(f"Strong load factor of {actual_load}% indicates supply-demand balance.")
    elif actual_load >= 75:
        parts.append(f"Acceptable load factor of {actual_load}% with opportunity for yield management.")
    else:
        parts.append(f"Load factor of {actual_load}% suggests potential overc apacity; review frequency.")
    
