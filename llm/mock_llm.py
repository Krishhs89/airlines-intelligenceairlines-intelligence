    def generate(
        self,
        template_key: str,
        variables: Dict[str, Any],
    ) -> str:
        """Generate a contextual response by filling a template with rules and variables.

        Args:
            template_key: Key into ``RESPONSE_TEMPLATES``.
            variables: Dict of values to inject into the template.

        Returns:
            The rendered response string with realistic insights.

        Raises:
            KeyError: If the template_key does not exist.
        """
        if template_key not in self.RESPONSE_TEMPLATES:
            raise KeyError(
                f"Unknown template key '{template_key}'. "
                f"Available: {list(self.RESPONSE_TEMPLATES.keys())}"
            )

        # Evaluate all rules and merge with supplied variables
        enriched: Dict[str, Any] = dict(variables)
        for rule_name, rule_fn in self.RULES.items():
            if rule_name not in enriched:
                try:
                    enriched[rule_name] = rule_fn(variables)
                except Exception as e:
                    enriched[rule_name] = "N/A"

        template = self.RESPONSE_TEMPLATES[template_key]
        try:
            response = template.format(**enriched)
            return response
        except KeyError as exc:
            return (
                f"[MockLLM] Template rendering incomplete -- missing "
                f"variable {exc}. Supplied keys: {list(enriched.keys())}"
            )

    def generate_from_query(
        self,
        query: str,
        context: Dict[str, Any],
    ) -> str:
        """Generate a response directly from a user query with context.
        
        This method infers the appropriate template and enriches context
        based on the query content.
        
        Args:
            query: Natural language user query.
            context: Contextual data from the system.
        
        Returns:
            A contextual response string.
        """
        intent = self.classify_intent(query)
        
        # Enrich context with query-specific insights
        enriched = dict(context)
        enriched["query"] = query
        enriched["generated_at"] = "realtime"
        
        # Extract specific entities from query for targeted responses
        if "vs" in query.lower() or "compare" in query.lower():
            enriched["comparison_mode"] = True
        if "forecast" in query.lower() or "predict" in query.lower():
            enriched["include_forecast"] = True
        if "why" in query.lower() or "reason" in query.lower():
            enriched["explain_reasoning"] = True
            
        return self.generate(intent, enriched)

    def classify_intent(self, query: str) -> str:
        """Intelligently classify a natural-language query into a system intent.

        Uses keyword matching with scoring to determine the most likely intent.
        Falls back to executive_summary for ambiguous queries.

        Args:
            query: User query string (case-insensitive).

        Returns:
            Intent string: ``route_analysis``, ``disruption_impact``,
            ``executive_summary``, ``schedule_gap``, or ``anomaly_report``.
        """
        query_lower = query.lower()

        intent_keywords: Dict[str, list[str]] = {
            "route_analysis": [
                "route", "demand", "frequency", "city pair", "market",
                "competition", "revenue", "network", "hub", "analyze route",
                "which route", "performance", "capacity",
            ],
            "disruption_impact": [
                "disruption", "weather", "storm", "delay", "cancel",
                "grounding", "irops", "irregular", "divert", "gate closure",
                "mechanical", "impact", "what if", "scenario", "cascade",
            ],
            "executive_summary": [
                "summary", "executive", "overview", "dashboard", "status",
                "kpi", "performance", "report", "how many", "show me",
            ],
            "schedule_gap": [
                "schedule", "gap", "opportunity", "underserved", "slot",
                "timing", "connection", "when", "expand to", "new route",
            ],
            "anomaly_report": [
                "anomaly", "anomalies", "unusual", "outlier", "spike",
                "abnormal", "alert", "warning", "wrong", "issue",
            ],
        }

        scores: Dict[str, int] = {}
        for intent, keywords in intent_keywords.items():
            scores[intent] = sum(1 for kw in keywords if kw in query_lower)

        best_intent = max(scores, key=scores.get)  # type: ignore[arg-type]
        if scores[best_intent] == 0:
            return "executive_summary"  # default fallback
        return best_intent

    def stream_response(
        self, text: str, delay: float = 0.03
    ) -> Generator[str, None, None]:
        """Yield words from *text* one at a time with a small delay.

        Simulates the streaming behaviour of a real LLM API for responsive UX.

        Args:
            text: The full response text to stream.
            delay: Seconds to wait between words (for realistic feel).

        Yields:
            One word (plus trailing space) at a time.
        """
        words = text.split()
        for i, word in enumerate(words):
            suffix = " " if i < len(words) - 1 else ""
            yield word + suffix
            time.sleep(delay)
